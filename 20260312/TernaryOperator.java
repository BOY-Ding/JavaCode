public class TernaryOperator {
	//7个位运算符
	//按位与 & ： 两位全为1，结果1 ，否则0
	//按位或 | ： 两位有一个为1，结果1 ，否则0
	//按位异或 ^ : 两位一个是0，一个是1，结果是1 ，否则是0
	//按位取反 ~： 0变1,1变0
	// >>	算术右移 低位溢出，符号位不变，并用符号位补溢出的高位
	// —————二进制解释：二进制/2/2:
	// 例如8:00001000 8>>2 = 8 / 2 / 2 = 2 
	// 例如2:00000010
	// <<	算术左移 符号位不变，低位补0
	// >>>  逻辑右移/无符号右移 低位溢出，高位补0


	// 2&3 = 010 & 011 = 010 = 2
	// ~-2 = 10000010 反码1111 1101 反码+1=补码 11111110 取反 0000 0001 = 1
	// ~2 = 00000010 取反就是 11111101(补码-还需要转成原码) 
	// 补码 = 反码 - 1： 反码：11111100
	// 原码 = 符号位不变其他位取反 = 10000011 = -3

	// 2|3 = 00000010 | 00000011 = 00000011 = 3
	// 2^3 = 00000010 ^ 00000011 = 00000001 = 1

	// ~-5 = ?
	// -5原码 10000000 00000000 00000000 00000101
	// -5反码 11111111 11111111 11111111 11111010
	// -5补码 11111111 11111111 11111111 11111011
	// 取反   00000000 00000000 00000000 00000100 正数三码合一 结果是 4

	// 13&7 = ?
	// 13转二进制 13 / 2 = 6-1 / 2 = 3-0 / 2 = 1 - 1 / 2 = 0 - 1 二进制就是1101
	// 13原码	00000000 00000000 00000000 00001101
	// 7原码 	00000000 00000000 00000000 00000111
	// 按位与	00000000 00000000 00000000 00000101 正数三码合一 结果是5

	// 5|4 = ?
	// 5原码 00000000 00000000 00000000 00000101
	// 4原码 00000000 00000000 00000000 00000100
	// 或    00000000 00000000 00000000 00000101 = 5

	// -3^3 = ?
	// -3原码 10000000 00000000 00000000 00000011
	// -3反码 11111111 11111111 11111111 11111100
	// -3补码 11111111 11111111 11111111 11111101
	// 3 原码 00000000 00000000 00000000 00000011
	// ^ 异或 11111111 11111111 11111111 11111110 - 负数补码需要转反码 - 原码
	// 反码   11111111 11111111 11111111 11111101
	// 取原码 10000000 00 000000 00000000 00000010 = -2

	public static void main (String[] args) {
		String a = "123.00";
		double b = Double.parseDouble(a);
		char c = 'a';
		String d = c + "";
		System.out.println("b:" + b + "d:" + d);
	}
}
